export interface MatchedLocation {
  city: string | null;
  area: string | null;
}

/**
 * Intelligent location matching from address string (supports English & Bangla)
 */
export function matchLocationFromAddress(address: string, cities: string[]): MatchedLocation {
  if (!address || address.length < 3) return { city: null, area: null };
  
  const addr = address.toLowerCase();
  let matchedCity: string | null = null;
  let matchedArea: string | null = null;

  // 1. Check for Cities (English)
  for (const city of cities) {
    if (addr.includes(city.toLowerCase())) {
      matchedCity = city;
      break;
    }
  }

  // 2. Check for Cities (Bangla) if not found in English
  if (!matchedCity) {
    for (const [bangla, english] of Object.entries(CITY_TRANSLATIONS)) {
      if (addr.includes(bangla)) {
        matchedCity = english;
        break;
      }
    }
  }

  // 3. Check for Areas (English) within the matched city
  if (matchedCity) {
    const areas = BANGLADESH_LOCATIONS[matchedCity] || [];
    for (const area of areas) {
      if (addr.includes(area.toLowerCase())) {
        matchedArea = area;
        break;
      }
    }
    
    // Check for Areas (Bangla) within the matched city
    if (!matchedArea) {
      for (const [bangla, english] of Object.entries(AREA_TRANSLATIONS)) {
        if (addr.includes(bangla)) {
          // Verify if this area belongs to the matched city (optional but safer)
          if (areas.includes(english)) {
            matchedArea = english;
            break;
          }
        }
      }
    }
  } else {
    // 4. Try matching Area directly (Bangla or English) to infer City
    for (const [bangla, english] of Object.entries(AREA_TRANSLATIONS)) {
      if (addr.includes(bangla)) {
        matchedArea = english;
        // Infer city from area
        for (const [city, cityAreas] of Object.entries(BANGLADESH_LOCATIONS)) {
          if (cityAreas.includes(english)) {
            matchedCity = city;
            break;
          }
        }
        if (matchedCity) break;
      }
    }

    if (!matchedCity) {
      // Check English areas directly to infer city
      for (const [city, cityAreas] of Object.entries(BANGLADESH_LOCATIONS)) {
        for (const area of cityAreas) {
          if (addr.includes(area.toLowerCase())) {
            matchedCity = city;
            matchedArea = area;
            break;
          }
        }
        if (matchedCity) break;
      }
    }
  }

  return { city: matchedCity, area: matchedArea };
}
